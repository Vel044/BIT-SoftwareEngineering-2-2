package example.chapter2;

import java.util.Scanner;

public class Ex212 {

	/**
	 * ๏ฟฝะถ๏ฟฝฤณ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝวท๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ
	 */
	public static void main(String[] args) {
		Scanner scn = new Scanner(System.in);		

		System.out.print("๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ:");
		int x=scn.nextInt();
		int div;
		
		for(div=2; div<=Math.sqrt(x); div++){
			if(x%div==0){//๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝdiv<=Math.sqrt(x)
				break;
			}
		}
		if(div>Math.sqrt(x)){//ศซ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝษจ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝฮด๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ
			System.out.println(x+"๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ");
		}else{
			System.out.println(x+"๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ");
		}
	}

}
