public class Ex22 {

	public static void main(String[] args) {		
		int  i = 10, j = 8, m = 11, n = 20, k, g;    
			
		System.out.println(i++);		//๏ฟฝาต๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝi๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ++๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝสฒรด
		System.out.println(++j);		//๏ฟฝาต๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝi๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ++๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝสฒรด
			
		System.out.println("i="+i);	
		System.out.println("j="+j);
			
		k = m++;			//๏ฟฝาต๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝm๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ++๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝสฒรด
		System.out.println("k="+k);	
		System.out.println("m="+m);
			
		g = 3*(++n);		//๏ฟฝาต๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝn๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ++๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝสฒรด
		System.out.println("g="+g);
		System.out.println("n="+n);
	}

}
